		          				<h3>Cedar High School History</h3>

		           				<p>
		           				<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Born in 1898 as a division of Branch Normal School, what would later become  Cedar High School originally operated under the auspices of the University of Utah.  But when the responsibility for running the school passed from the University of Utah  to Utah State University in 1913, the school became the Branch Agricultural  College--the progenitor of today's Southern Utah University. As the town grew and  the number of students enrolled in the school swelled, administrators recognized  the need for an independent high school. Hence, the creation of Cedar High  School, where classes began in September 1940.  </p>
		           				<p>Located on Center and 300 West, this new school could accommodate 350  students. However, by the early 1960s, a student body topping 500 students  crowded classrooms and congested hallways. Consequently, the Iron County  School District spent just over $3 million to construct a larger high school located at  703 West 600 South. A state-of-the-art edifice, the new school opened its doors for  the 1963-64 academic years with facilities for individualized programs for all  students. (Although the old Cedar High School served as a junior-high for decades,  administrators at Southern Utah University eventually acquired the building and in  2000 razed it to make a new parking lot.)</p>
		           				<p>The growth of the Cedar High School student body hardly ended in the Sixties.  To deal with this growth, in 1988 administrators added new classrooms on the south  side of D Hall. Expansion continued in 1993, when administrators not only erected  the current auxiliary gymnasium, ceramics, woodshop, and commons (now  lunchroom) areas, but also added a basement to the school, so opening more  classroom space. Taking even more dramatic action, in 1997 administrators sent  approximately half of the student body to a new high school, Canyon View High  School, built on the north end of town. To accommodate further growth--and to  modernize the school's facilities--the Iron County School Board authorized  dramatically remodeling the school in 2010, so launching a two-year construction  project updating the school's lighting, electrical, and heating systems, and adding  a suite of administrative offices, a new commons, a band room, a faculty room,  and a biology lab. </p>
