		          				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;In 1942 the student council led by president Jay Thorley adopted the name &quot;Redmen&quot; representing the many Native Americans who lived and continue to live in this area. The name was originally chosen for the athletic teams, but over time prepresented the whole student body.</p>
		          				<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The mascot, an Indian shield, has changed throughout the history of Cedar High School, but has always been true to its roots. Cedar High School students are expected to be &quot;proud to be a Redmen!&quot; The mascot, Redmen, reminds all of us of the pride, nobility, and hard work it takes to achieve success. </p>
		          				<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The school colors also have significant meaning behind them. Gold symbolizes the sun, representing light and knowledge. Crimson signifies strength and loyalty, characteristics that all CHS Redmen should strive for their lives. </p>
