<h3>What is the Hunt?</h3>
<p>The Hunt is a game, created by Josh A. and Kirby M. at Cedar High School. It should be a fun thing to do in between classes or when you have spare time. The main goal of the Hunt is to be fun.</p>
<h3>What are the prizes?</h3>
<p>Whoever is at the top of the scoreboard at a time to be determined will receive a $50 Amazon gift card and a custom 3D-printed trophy with their name on it.</p>
<h3>How do I play?</h3>
<p>Your goal in the Hunt is to get as many points as you can. There are multiple ways to earn points;
<ul>
   <li>Solve hints to get codes</li>
   <li>Complete quizzes</li>
   <li>Find secret codes around the school</li>
   <li>Earn codes from people who have them</li>
   <li>Ask around for people with free codes</li>
</ul>
</p>
<h3>What are the rules?</h3>
<p>
   The rules for the Hunt are as follows;
   <ol>
      <li>Don't cheat - we can and have found out and you'll be disqualified</li>
      <li>Usernames must be school appropriate</li>
      <li>Don't remove physical codes - please do not mess with the codes you find around school, you'll ruin it for everyone</li>
   </ol>
</p>
<h3>How does the point system work?</h3>
<p>
   The point system is pretty simple. Each code has a set amount of points assigned to it. The first player to put in the code will receive twice the amount of points for the code, and the first 10 players after the first will receive 1 1/2 times the set amount. This is why you will want to try to get codes as soon as you can to get maximum points.
</p>
